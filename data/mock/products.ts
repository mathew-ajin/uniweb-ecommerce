interface PD {
  id: string;
  s: string;
  b: [string, string];
  n: [string, string];
  c: string;
  sc: string;
  pt?: string;
  g?: "women" | "men" | "unisex";
  p: number;
  op?: number;
  ik: string;
  cols?: string[];
  sz?: "c" | "s";
  nw?: boolean;
  bs?: boolean;
  ft?: boolean;
  sl?: boolean;
  rt: number;
  rc: number;
  rel?: string[];
  rv?: ReviewItem[];
}

function mk(d: PD): ProductItem {
  const colors = (d.cols || []).map((k) => C[k]).filter(Boolean);
  const sizes = d.sz === "c" ? [...SZ_CL] : d.sz === "s" ? [...SZ_SH] : [];
  const baseIdx = Math.max(0, IMG_KEYS.indexOf(d.ik));
  const primaryUrl = IMG[d.ik] || IMG_ARR[0];

  // Color-specific images — each color gets a different mock image for visual variety
  const imgs: ProductImage[] = [];
  if (colors.length > 0) {
    colors.forEach((color, i) => {
      imgs.push({
        id: `${d.id}-${color.id}`,
        url: IMG_ARR[(baseIdx + i) % IMG_ARR.length],
        alt: { en: `${d.n[0]} - ${color.name.en}`, ar: `${d.n[1]} - ${color.name.ar}` },
        isPrimary: i === 0,
        colorId: color.id,
      });
    });
  } else {
    imgs.push({ id: `${d.id}-1`, url: primaryUrl, alt: { en: d.n[0], ar: d.n[1] }, isPrimary: true });
  }

  // Full variant grid — every color × size combination
  const variants: ProductVariant[] = [];
  if (colors.length > 0 && sizes.length > 0) {
    for (const color of colors) {
      for (const size of sizes) {
        variants.push({
          id: `${d.id}-${color.id}-${size.id}`,
          colorId: color.id,
          sizeId: size.id,
          sku: `MYR-${d.id}-${color.id}-${size.id}`.toUpperCase(),
          stock: size.stock,
          price: d.p,
          compareAtPrice: d.op,
          images: [`${d.id}-${color.id}`],
        });
      }
    }
  } else if (colors.length > 0) {
    for (const color of colors) {
      variants.push({
        id: `${d.id}-${color.id}`,
        colorId: color.id,
        sku: `MYR-${d.id}-${color.id}`.toUpperCase(),
        stock: 25,
        price: d.p,
        compareAtPrice: d.op,
        images: [`${d.id}-${color.id}`],
      });
    }
  } else {
    variants.push({
      id: `${d.id}-v1`,
      colorId: "",
      sku: `MYR-${d.id}`.toUpperCase(),
      stock: 25,
      price: d.p,
      compareAtPrice: d.op,
      images: [imgs[0]?.id || ""],
    });
  }

  return {
    id: d.id,
    slug: d.s,
    brand: { en: d.b[0], ar: d.b[1] },
    name: { en: d.n[0], ar: d.n[1] },
    shortDescription: { en: `Premium ${d.n[0].toLowerCase()} by ${d.b[0]}`, ar: `${d.n[1]} الفاخر من ${d.b[1]}` },
    fullDescription: {
      en: `Discover the ${d.n[0]} from ${d.b[0]}. Crafted with exceptional attention to detail and premium materials, this piece embodies modern elegance perfect for any occasion.`,
      ar: `اكتشف ${d.n[1]} من ${d.b[1]}. صُنع بعناية فائقة بمواد فاخرة، يجسد الأناقة العصرية المثالية لكل مناسبة.`,
    },
    categoryId: d.c,
    subcategoryId: d.sc,
    productTypeId: d.pt,
    gender: d.g,
    images: imgs,
    colors,
    sizes,
    variants,
    basePrice: d.p,
    compareAtPrice: d.op,
    rating: d.rt,
    reviewCount: d.rc,
    isNew: d.nw,
    isBestSeller: d.bs,
    isFeatured: d.ft,
    isOnSale: d.sl,
    inStock: true,
    specifications: [
      { label: { en: "Material", ar: "الخامة" }, value: { en: "Premium quality", ar: "جودة عالية" } },
      { label: { en: "Origin", ar: "المنشأ" }, value: { en: "Imported", ar: "مستورد" } },
    ],
    shippingInfo: {
      en: "Free delivery on orders above KWD 20. Standard delivery 2-4 business days.",
      ar: "توصيل مجاني للطلبات فوق 20 د.ك. التوصيل العادي 2-4 أيام عمل.",
    },
    returnInfo: {
      en: "Easy returns within 14 days. Items must be unworn with original tags.",
      ar: "إرجاع سهل خلال 14 يوماً. يجب أن تكون القطع غير مستخدمة.",
    },
    careInstructions: [{ en: "Follow garment care label", ar: "اتبع تعليمات العناية" }],
    reviews: d.rv || [],
    relatedProductIds: d.rel || [],
  };
}

